function hauteur() {
    var div_princ = document.getElementById("div_princ");
    
    if (div_princ.style.height === "600px") {
        // Si la hauteur est déjà de 600px, réduire à 100px
        div_princ.style.height = "100px";
    } else {
        // Sinon, agrandir à 600px
        div_princ.style.height = "600px";
    }
}
